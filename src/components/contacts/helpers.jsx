let getRandom = function (array) {
  return array[Math.floor(Math.random() * array.length)];
};

export let getRandomName = () => {
  return getRandom(names);
};
export let getRandomCompany = () => {
  return getRandom(companyNames);
};
export let getRandomJobTitle = () => {
  return getRandom(jobTitles);
};

// get random integer number between min and max (both included)

export let getRandomInt = (min, max) => {
  return Math.floor(Math.random() * (max - min + 1)) + min;
};

export const findSegementType = (label) => {
  if (label === 0) {
    return "Punctual customers";
  } else if (label === 1) {
    return "Hibernating customers";
  } else if (label === 2) {
    return "Exceptional customers";
  } else if (label === 3) {
    return "Recent customers";
  }
};

export const findSegementClass = (segementType) => {
  switch (segementType) {
    case "Punctual customers":
      return "punctualCustomers";
    case "Hibernating customers":
      return "hibernatingCustomers";
    case "Exceptional customers":
      return "exceptionalCustomers";
    case "Recent customers":
      return "recentCustomers";
    default:
  }
};

export default {
  findSegementType,
  findSegementClass,
  getRandomInt,
  getRandomName,
  getRandomCompany,
  getRandomJobTitle,
};
